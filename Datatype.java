class Datatype
{
    public static void main(String arg[])
    {

        int no = 11;
        float f = 22.1f;
        double d = 29.8989898;
        char ch = 'c';

        System.out.println(no);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);

    }
}